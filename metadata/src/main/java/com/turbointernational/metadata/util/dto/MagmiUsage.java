package com.turbointernational.metadata.util.dto;

/**
 *
 * @author jrodriguez
 */
public class MagmiUsage {

    private Long principalId;

    private Long sku;

    private String manufacturer;

    private String partNumber;

    private Long tiSku;

    private String tiPartNumber;

    private String partType;

    private String turboType;

    private String turboPartNumber;

    /**
     * @return the principalId
     */
    public Long getPrincipalId() {
        return principalId;
    }

    /**
     * @param principalId the principalId to set
     */
    public void setPrincipalId(Long principalId) {
        this.principalId = principalId;
    }

    /**
     * @return the sku
     */
    public Long getSku() {
        return sku;
    }

    /**
     * @param sku the sku to set
     */
    public void setSku(Long sku) {
        this.sku = sku;
    }

    /**
     * @return the manufacturer
     */
    public String getManufacturer() {
        return manufacturer;
    }

    /**
     * @param manufacturer the manufacturer to set
     */
    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    /**
     * @return the partNumber
     */
    public String getPartNumber() {
        return partNumber;
    }

    /**
     * @param partNumber the partNumber to set
     */
    public void setPartNumber(String partNumber) {
        this.partNumber = partNumber;
    }

    /**
     * @return the tiSku
     */
    public Long getTiSku() {
        return tiSku;
    }

    /**
     * @param tiSku the tiSku to set
     */
    public void setTiSku(Long tiSku) {
        this.tiSku = tiSku;
    }

    /**
     * @return the tiPartNumber
     */
    public String getTiPartNumber() {
        return tiPartNumber;
    }

    /**
     * @param tiPartNumber the tiPartNumber to set
     */
    public void setTiPartNumber(String tiPartNumber) {
        this.tiPartNumber = tiPartNumber;
    }

    /**
     * @return the partType
     */
    public String getPartType() {
        return partType;
    }

    /**
     * @param partType the partType to set
     */
    public void setPartType(String partType) {
        this.partType = partType;
    }

    /**
     * @return the turboType
     */
    public String getTurboType() {
        return turboType;
    }

    /**
     * @param turboType the turboType to set
     */
    public void setTurboType(String turboType) {
        this.turboType = turboType;
    }

    /**
     * @return the turboPartNumber
     */
    public String getTurboPartNumber() {
        return turboPartNumber;
    }

    /**
     * @param turboPartNumber the turboPartNumber to set
     */
    public void setTurboPartNumber(String turboPartNumber) {
        this.turboPartNumber = turboPartNumber;
    }

}
